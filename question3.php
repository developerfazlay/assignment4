/******************************
 * Factorial using Increement
 * ****************************/ 

$factorial_number = 7;
$factorial_value = 1;

for ($i=1; $i<=$factorial_number; $i++) { 
    $factorial_value *= $i; 
}

echo "<br>";
echo "{$factorial_number}! = ". $factorial_value;








